// Primitive datatypes

let x = 42; // Number	Any integer or floating-point number
let name = "Alice"; // String   Text enclosed in quotes
let isCool = true; // Boolean   Either true or false
let y; // undefined	  A variable declared but not assigned
let z = null; // Null	    An intentional empty value
let big = 12345678901234567890n; // BigInt	Large numbers beyond Number.MAX_SAFE_INTEGER
let id = Symbol("unique"); // Symbol	Unique identifier, useful in objects

console.log(typeof "name");
console.log(typeof null); // object
console.log(typeof undefined); // undefined
